    private void btnSalvarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarUsuarioActionPerformed
        if (flagEditarUsuario != 1) {
            try {
                this.incluirUsuario();
                this.home.carregarTabelaDeUsuarios();
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(FormAddVeiculo.class.getName()).log(Level.SEVERE, null, ex);
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(FormAddVeiculo.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(FormAddVeiculo.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                this.editarUsuario();
            } catch (SQLException ex) {
                Logger.getLogger(FormAddUsuario.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(FormAddUsuario.class.getName()).log(Level.SEVERE, null, ex);
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(FormAddUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.flagEditarUsuario = 0;
        }
    }//GEN-LAST:event_btnSalvarUsuarioActionPerformed

