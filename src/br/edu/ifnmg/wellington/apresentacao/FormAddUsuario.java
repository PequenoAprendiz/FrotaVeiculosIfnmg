    private void incluirUsuario() throws NoSuchAlgorithmException, UnsupportedEncodingException, SQLException {
        try {
            this.usuarioBO = new UsuarioBO();
            this.recuperarCamposFormNovoUsuario();
            this.usuarioBO.verificauUsuarioDuplicado(this.usuarioEmEdicao);
            this.usuarioBO.incluirUsuario(this.usuarioEmEdicao);
            JOptionPane.showMessageDialog(this, "Cadastrado com sucesso!", "Novo usuário", JOptionPane.INFORMATION_MESSAGE);
            this.limparCamposFormNovoUsuario();
        } catch (CampoObrigatorioException c) {
            String mensagen = "Por favor, Preencha todos os campos!\n" + c.getMessage();
            JOptionPane.showMessageDialog(this, mensagen, "Novo usuario", JOptionPane.ERROR_MESSAGE);
        } catch (VerificarUsuarioDuplicado v) {
            JOptionPane.showMessageDialog(this, v.getMessage(), "Novo usuário", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void editarUsuario() throws SQLException, NoSuchAlgorithmException, UnsupportedEncodingException {
        //    try {
        this.usuarioBO = new UsuarioBO();
        int i = JOptionPane.showConfirmDialog(this, "Tem Certeza?", "Edição de usuário", JOptionPane.YES_NO_OPTION);
        if (i == JOptionPane.NO_OPTION) {
            this.dispose();
        } else {
            this.recuperarCamposFormNovoUsuario();
            this.usuarioBO.atualizarUsuario(this.usuarioEmEdicao);
            JOptionPane.showMessageDialog(this, "Alterado com sucesso!", "Edição de veículo", JOptionPane.INFORMATION_MESSAGE);
            this.limparCamposFormNovoUsuario();
            this.home.carregarTabelaDeUsuarios();
            this.dispose();
        }
//        } catch (RuntimeException r) {
//            JOptionPane.showMessageDialog(this, "algum campo está vazio", "Edição de veículo", JOptionPane.INFORMATION_MESSAGE);
//        }
    }

    private void limparCamposFormNovoUsuario() {
        this.txtAddLogin.setText("");
        this.txtAddSenhaUsuario.setText("");
        this.cbxTipoUser.setSelectedIndex(0);
    }

    private void recuperarCamposFormNovoUsuario() {
        if (this.usuarioEmEdicao == null) {
            this.usuarioEmEdicao = new Usuario();
        }

        if (txtAddLogin.getText().equals("")) {
            throw new CampoObrigatorioException("Campo LOGIN vazio!");
        } else {
            this.usuarioEmEdicao.setLogin(txtAddLogin.getText());
        }

        if (txtAddSenhaUsuario.getText().equals("")) {
            throw new CampoObrigatorioException("Campo SENHA vazio!");
        } else {
            this.usuarioEmEdicao.setSenha(txtAddSenhaUsuario.getText());
        }

        if (cbxTipoUser.getSelectedItem().equals("--")) {
            throw new CampoObrigatorioException("Selecione o tipo de usuário");
        } else if (cbxTipoUser.getSelectedItem().equals("Administrador")) {
            this.usuarioEmEdicao.setTipo(1);
        } else {
            this.usuarioEmEdicao.setTipo(0);
        }

    }

    private void preencherCamposTela(Usuario usuarioEmEdicao) {
        this.txtAddLogin.setText(usuarioEmEdicao.getLogin());
        this.txtAddSenhaUsuario.setText("");
        this.lblDica.setText("se não for alterar não preencha este campo!");
        this.cbxTipoUser.setSelectedItem(usuarioEmEdicao.getTipo());
    }

    private void limparCamposTelaNovoUsuario() {
        this.txtAddLogin.setText("");
        this.txtAddSenhaUsuario.setText("");
        this.lblDica.setText("");
        this.cbxTipoUser.setSelectedItem(0);
    }

